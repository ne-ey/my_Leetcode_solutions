from collections import deque
from typing import Optional

# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right

class Solution:
    def maxLevelSum(self, root: Optional[TreeNode]) -> int:
      
        if not root:
            return 0

        queue = deque([root])
        level = 1
        max_sum = float('-inf')
        answer_level = 1

        while queue:
            current_sum = 0
            size = len(queue)

            for _ in range(size):
                node = queue.popleft()
                current_sum += node.val

                if node.left:
                    queue.append(node.left)
                if node.right:
                    queue.append(node.right)

            if current_sum > max_sum:
                max_sum = current_sum
                answer_level = level

            level += 1

        return answer_level
